def test_emit_contract_tests_writes_importable_contract_modules(tmp_path: Path) -> None:
    catalog = load_catalog(FIX)
    project_root = tmp_path
    package_root = project_root / "stonepy"
    package_root.mkdir()
    (package_root / "__init__.py").write_text(
        "from pkgutil import extend_path\n__path__ = extend_path(__path__, __name__)\n",
        encoding="utf-8",
    )

    emit_models(catalog, package_root)
    emit_endpoints(catalog, package_root)

    stale = project_root / "tests" / "contract" / "stale.py"
    stale.parent.mkdir(parents=True)
    stale.write_text("SHOULD_BE_REMOVED = True\n", encoding="utf-8")

    emit_contract_tests(catalog, project_root)

    roundtrip = project_root / "tests" / "contract" / "test_models_roundtrip.py"
    endpoint_specs = project_root / "tests" / "contract" / "test_endpoint_specs.py"
    lookup_enums = project_root / "tests" / "contract" / "test_lookup_enums.py"
    assert roundtrip.exists()
    assert endpoint_specs.exists()
    assert lookup_enums.exists()
    assert not stale.exists()
    roundtrip_text = roundtrip.read_text(encoding="utf-8")
    endpoint_specs_text = endpoint_specs.read_text(encoding="utf-8")
    lookup_enums_text = lookup_enums.read_text(encoding="utf-8")
    assert "GENERATED by stonepy._generator" in roundtrip_text
    assert "GENERATED by stonepy._generator" in endpoint_specs_text
    assert "GENERATED by stonepy._generator" in lookup_enums_text
    assert "from stonepy._core.models import StoneXModel" in roundtrip_text
    assert "model_cls: type[StoneXModel]" in roundtrip_text
    assert "from stonepy._core.endpoint import EndpointSpec" in endpoint_specs_text
    assert "spec: EndpointSpec[ResponseModel]" in endpoint_specs_text
    assert "enum_cls: type[IntEnum]" in lookup_enums_text

    stonepy_modules = _snapshot_stonepy_modules()
    _clear_stonepy_modules()
    sys.path.insert(0, str(project_root))
    try:
        _import_generated_module(roundtrip, "generated_test_models_roundtrip")
        _import_generated_module(endpoint_specs, "generated_test_endpoint_specs")
        _import_generated_module(lookup_enums, "generated_test_lookup_enums")
    finally:
        sys.path.remove(str(project_root))
        _restore_stonepy_modules(stonepy_modules)


def test_cli_all_writes_package_outputs_and_contract_tests_from_parent_catalog_root(
    tmp_path: Path,
) -> None:
    docs_root = tmp_path / "Docs"
    catalog_root = docs_root / "catalog"
    catalog_root.mkdir(parents=True)
    for filename in ("endpoints.json", "data-types.json", "lookup-codes.json"):
        (catalog_root / filename).write_text((FIX / filename).read_text(encoding="utf-8"))

