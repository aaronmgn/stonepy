def _import_generated_module(path: Path, module_name: str) -> None:
    spec = importlib.util.spec_from_file_location(module_name, path)
    assert spec is not None
    assert spec.loader is not None
    module = importlib.util.module_from_spec(spec)
    sys.modules[module_name] = module
    try:
        spec.loader.exec_module(module)
    finally:
        del sys.modules[module_name]


def _snapshot_stonepy_modules() -> dict[str, ModuleType]:
    return {
        name: module
        for name, module in sys.modules.items()
        if name == "stonepy" or name.startswith("stonepy.")
    }


def _clear_stonepy_modules() -> None:
    for module_name in list(_snapshot_stonepy_modules()):
        del sys.modules[module_name]


def _restore_stonepy_modules(snapshot: dict[str, ModuleType]) -> None:
    _clear_stonepy_modules()
    sys.modules.update(snapshot)


def _endpoint(
    name: str,
    *,
    target: str,
    path: str,
    method: str = "GET",
    response_type: str | None = None,
) -> EndpointRecord:
    return EndpointRecord(
        name=name,
        logical_name=name,
        version="v1",
        description=None,
        method=method,
        target=target,
        uri_template=path,
        path=path,
        content_type="application/json",
        envelope="URL",
        parameters=[],
        request_type=None,
        response_type=response_type,
        source_url=None,
        source_file=None,
        last_updated=None,
        raw={"name": name},
    )


def _datatype(name: str, properties: list[dict[str, object]]) -> TypeRecord:
    return TypeRecord(
        name=name,
        catalog_name=name,
        version="v1",
        description=None,
        properties=properties,
        source_url=None,
        source_file=None,
        last_updated=None,
        raw={"name": name, "properties": properties},
    )
