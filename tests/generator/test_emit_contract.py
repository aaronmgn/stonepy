    package_root.mkdir(parents=True)
    (package_root / "__init__.py").write_text("", encoding="utf-8")

    assert (
        main(
            [
                "all",
                "--catalog-root",
                str(docs_root),
                "--package-dir",
                str(package_root),
                "--project-root",
                str(project_root),
                "--allow-unresolved",
                "--allow-unfrozen-catalog",
            ]
        )
        == 0
    )

    assert (package_root / "models" / "AlertDTO.py").exists()
    assert (package_root / "_endpoints" / "order.py").exists()
    assert (project_root / "tests" / "contract" / "test_models_roundtrip.py").exists()
    assert (project_root / "tests" / "contract" / "test_endpoint_specs.py").exists()
    assert (project_root / "tests" / "contract" / "test_lookup_enums.py").exists()


