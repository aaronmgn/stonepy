def test_recursive_required_model_graph_roundtrips_via_validate_after_cycle_break(
    tmp_path: Path,
) -> None:
    first_name = "FirstRecursiveModelWithRequiredPeerDTO"
    second_name = "SecondRecursiveModelWithRequiredPeerDTO"
    catalog = Catalog(
        endpoints=[],
        datatypes=[
            _datatype(
                first_name,
                [
                    {
                        "name": "Second",
                        "type": second_name,
                        "format": None,
                        "ref": second_name,
                    }
                ],
            ),
            _datatype(
                second_name,
                [
                    {
                        "name": "First",
                        "type": first_name,
                        "format": None,
                        "ref": first_name,
                    }
                ],
            ),
        ],
        lookups={},
        unresolved=set(),
    )

    emit_contract_tests(catalog, tmp_path)

    rendered = (tmp_path / "tests" / "contract" / "test_models_roundtrip.py").read_text(
        encoding="utf-8"
    )
    # Mutually-recursive required model refs are emitted optional (cycle-broken in
    # emit_models._cyclic_ref_fields), so the roundtrip test validates them normally rather than
    # falling back to model_construct.
    assert "requires recursive model payload" not in rendered
    assert "pytest.skip(skip_reason)" not in rendered
    assert "required recursive model graph has no finite validating JSON payload" not in rendered
    assert "model_construct" not in rendered
    assert '@pytest.mark.parametrize("model_cls, payload", MODEL_CASES)' in rendered
    assert "model_validate" in rendered
    assert first_name in rendered and second_name in rendered
    assert max(len(line) for line in rendered.splitlines()) <= 100


