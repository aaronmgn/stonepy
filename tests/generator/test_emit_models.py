def test_direct_render_model_uses_enums_module_for_known_enum_refs(tmp_path: Path) -> None:
    cat = load_catalog(FIX)
    alert = next(rec for rec in cat.datatypes if rec.name == "AlertDTO")
    alert_direction = next(rec for rec in cat.datatypes if rec.name == "AlertDirection")

    package = tmp_path / "models"
    package.mkdir()
    (package / "__init__.py").write_text("", encoding="utf-8")
    (package / "AlertDTO.py").write_text(
        render_model(alert, {rec.name for rec in cat.datatypes}),
        encoding="utf-8",
    )
    (package / "enums.py").write_text(render_enum(alert_direction), encoding="utf-8")

    for module_name in [
        name for name in sys.modules if name == "models" or name.startswith("models.")
    ]:
        del sys.modules[module_name]
    sys.path.insert(0, str(tmp_path))
    try:
        module = importlib.import_module("models.AlertDTO")
        enums = importlib.import_module("models.enums")
    finally:
        sys.path.remove(str(tmp_path))

    import typing as _t

    assert enums.AlertDirection in _t.get_args(module.AlertDTO.model_fields["direction"].annotation)


def test_emit_all_generated_package_imports_with_circular_model_refs(
    tmp_path: Path,
) -> None:
    first = _datatype(
        "FirstDTO",
        [{"name": "Second", "type": "SecondDTO", "format": None, "ref": "SecondDTO"}],
    )
    second = _datatype(
        "SecondDTO",
        [{"name": "First", "type": "FirstDTO", "format": None, "ref": "FirstDTO"}],
    )
    catalog = Catalog(
        endpoints=[_endpoint(response_type="FirstDTO")],
        datatypes=[first, second],
        lookups={},
        unresolved=set(),
    )

    emit_all(catalog, tmp_path)

    first_text = (tmp_path / "models" / "FirstDTO.py").read_text(encoding="utf-8")
    assert "from typing import TYPE_CHECKING" in first_text
    assert "if TYPE_CHECKING:" in first_text
    assert "    from .SecondDTO import SecondDTO" in first_text
    assert first_text.count("from .SecondDTO import SecondDTO") == 1

    for module_name in [
        name for name in sys.modules if name == "models" or name.startswith("models.")
    ]:
        del sys.modules[module_name]
    sys.path.insert(0, str(tmp_path))
    try:
        imported = importlib.import_module("models")
    finally:
        sys.path.remove(str(tmp_path))

    assert imported.FirstDTO.__name__ == "FirstDTO"
    assert imported.SecondDTO.__name__ == "SecondDTO"
