def test_emit_all_renders_id_column_lookup_tables_as_enums(tmp_path: Path) -> None:
    # Lookup tables whose code column is named "<Thing>Id"/"<Thing> ID" (rather than
    # "<Thing> Code") must still become enums; the column-name heuristic looks for an id
    # column when no "code" column is present.
    catalog = Catalog(
        endpoints=[],
        datatypes=[],
        lookups={
            "OrderActionTypeId": {
                "columns": ["OrderActionTypeId", "Description"],
                "rows": [
                    {"OrderActionTypeId": "1", "Description": "Opening Order"},
                    {"OrderActionTypeId": "2", "Description": "Full Close"},
                ],
            },
            "CurrencyID": {
                "columns": ["Currency ID", "ISO Currency", "Description"],
                "rows": [
                    {"Currency ID": "1", "ISO Currency": "AUD", "Description": "Australian Dollar"},
                    {"Currency ID": "2", "ISO Currency": "CAD", "Description": "Canadian Dollar"},
                ],
            },
        },
        unresolved=set(),
    )

    emit_all(catalog, tmp_path)

    enums = (tmp_path / "models" / "enums.py").read_text(encoding="utf-8")
    init_text = (tmp_path / "models" / "__init__.py").read_text(encoding="utf-8")
    assert "class OrderActionTypeId(IntEnum):" in enums
    assert "OpeningOrder = 1" in enums
    assert "FullClose = 2" in enums
    assert "class CurrencyID(IntEnum):" in enums
    assert "AustralianDollar = 1" in enums
    assert "from .enums import" in init_text
    assert "CurrencyID" in init_text
    assert "OrderActionTypeId" in init_text


