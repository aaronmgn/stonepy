def test_emit_all_writes_models_enums_init_and_uses_endpoint_request_context(
    tmp_path: Path,
) -> None:
    request = _datatype(
        "SubmitAlertDTO",
        [{"name": "AlertId", "type": "Integer", "format": None, "ref": None}],
    )
    response = _datatype(
        "AlertDTO",
        [{"name": "AlertId", "type": "Integer", "format": None, "ref": None}],
    )
    direction = _datatype(
        "AlertDirection",
        [
            {"name": "Buy", "type": "1", "format": None, "ref": None},
            {"name": "Sell", "type": "2", "format": None, "ref": None},
        ],
    )
    catalog = Catalog(
        endpoints=[_endpoint(request_type="SubmitAlertDTO", response_type="AlertDTO")],
        datatypes=[request, response, direction],
        lookups={},
        unresolved=set(),
    )

    emit_all(catalog, tmp_path)
    first_init = (tmp_path / "models" / "__init__.py").read_text(encoding="utf-8")
    emit_all(catalog, tmp_path)

    assert (tmp_path / "models" / "AlertDTO.py").exists()
    assert (tmp_path / "models" / "SubmitAlertDTO.py").exists()
    assert (tmp_path / "models" / "enums.py").exists()
    assert (tmp_path / "models" / "__init__.py").read_text(encoding="utf-8") == first_init
    assert "class SubmitAlertDTO(RequestModel):" in (
        tmp_path / "models" / "SubmitAlertDTO.py"
    ).read_text(encoding="utf-8")
    assert "class AlertDTO(ResponseModel):" in (tmp_path / "models" / "AlertDTO.py").read_text(
        encoding="utf-8"
    )
    assert "class AlertDirection(IntEnum):" in (tmp_path / "models" / "enums.py").read_text(
        encoding="utf-8"
    )
    assert "from .AlertDTO import AlertDTO" in first_init
    assert "from .SubmitAlertDTO import SubmitAlertDTO" in first_init
    assert "from .enums import AlertDirection" in first_init


