FIX = Path(__file__).parent / "fixtures"


def _datatype(
    name: str,
    properties: list[dict[str, object]],
) -> TypeRecord:
    return TypeRecord(
        name=name,
        catalog_name=name,
        version="v1",
        description=None,
        properties=properties,
        source_url=None,
        source_file=None,
        last_updated=None,
        raw={"name": name, "properties": properties},
    )


def _endpoint(request_type: str | None = None, response_type: str | None = None) -> EndpointRecord:
    return EndpointRecord(
        name="FixtureEndpoint",
        logical_name="FixtureEndpoint",
        version="v1",
        description=None,
        method="POST",
        target=None,
        uri_template="/fixture",
        path="/fixture",
        content_type="application/json",
        envelope="JSON",
        parameters=[],
        request_type=request_type,
        response_type=response_type,
        source_url=None,
        source_file=None,
        last_updated=None,
        raw={"name": "FixtureEndpoint"},
    )


