"""Render generated model source text from catalog records."""

from __future__ import annotations

import json
import keyword
import re
import subprocess
import tempfile
from collections.abc import Collection, Mapping
from importlib import import_module
from pathlib import Path
from typing import Any, cast

from stonepy._generator.catalog import JsonObject, TypeRecord, python_name, python_type

BANNER = "# GENERATED by stonepy._generator. Do not edit by hand.\n"

_IDENTIFIER_RE = re.compile(r"[A-Za-z_][A-Za-z0-9_]*")
_LOWER_TO_UPPER_RE = re.compile(r"([a-z0-9])([A-Z])")
_WORD_BOUNDARY_RE = re.compile(r"(.)([A-Z][a-z]+)")


def format_python(source: str) -> str:
    """Return Ruff-formatted and import-sorted Python source."""

    try:
        ruff = cast(Any, import_module("ruff"))
    except ImportError as exc:
        raise RuntimeError("ruff is required to format generated source") from exc

    ruff_bin = ruff.find_ruff_bin()
    formatted = _format_with_ruff(source, ruff_bin)
    formatted = _sort_imports_with_ruff(formatted, ruff_bin)
    return _format_with_ruff(formatted, ruff_bin)


def _format_with_ruff(source: str, ruff_bin: str) -> str:
    with tempfile.TemporaryDirectory() as tmp:
        path = Path(tmp) / "generated.py"
        path.write_text(source, encoding="utf-8")
        result = subprocess.run(
            [ruff_bin, "format", str(path)],
            check=False,
            capture_output=True,
            text=True,
        )
        if result.returncode != 0:
            detail = result.stderr or result.stdout
            raise RuntimeError(f"ruff failed to format generated source:\n{detail}")
        return path.read_text(encoding="utf-8")


def _sort_imports_with_ruff(source: str, ruff_bin: str) -> str:
    with tempfile.TemporaryDirectory() as tmp:
        path = Path(tmp) / "generated.py"
        path.write_text(source, encoding="utf-8")
        result = subprocess.run(
            [ruff_bin, "check", "--fix", "--select", "I", str(path)],
            check=False,
            capture_output=True,
            text=True,
        )
        if result.returncode != 0:
            detail = result.stderr or result.stdout
            raise RuntimeError(f"ruff failed to sort generated imports:\n{detail}")
        return path.read_text(encoding="utf-8")


def field_name(catalog_name: str) -> str | None:
    """Return a snake_case Python field name, or None for blank placeholders."""

    stripped = catalog_name.strip()
    if not stripped:
        return None

    candidate = re.sub(r"[^0-9A-Za-z_]+", "_", stripped)
    candidate = _WORD_BOUNDARY_RE.sub(r"\1_\2", candidate)
    candidate = _LOWER_TO_UPPER_RE.sub(r"\1_\2", candidate)
    candidate = re.sub(r"_+", "_", candidate).strip("_").lower()
    if not candidate:
        return None
    if candidate[0].isdigit():
        candidate = f"_{candidate}"
    if keyword.iskeyword(candidate):
        candidate = f"{candidate}_"
    return candidate


def render_model(
    rec: TypeRecord,
    known_names: set[str],
    *,
    request_types: Collection[str] | None = None,
    enum_names: Collection[str] | None = None,
    force_optional: Collection[str] | None = None,
) -> str:
    """Render a generated Pydantic model class for a datatype record."""

    base_model = _base_model(rec, request_types)
    # Response payloads from the (WCF) API serialize absent reference members as ``null`` and may
    # omit fields entirely, so response-model fields must all tolerate None/missing. Request models
    # keep catalog optionality so required inputs are still enforced for callers.
    fields = _model_fields(
        rec,
        known_names,
        force_optional=set(force_optional) if force_optional is not None else None,
        all_optional=base_model == "ResponseModel",
    )
    annotations = [field.annotation for field in fields]
    imports = _model_imports(
        annotations=annotations,
        base_model=base_model,
        uses_field=bool(fields),
        uses_unresolved=any(field.uses_unresolved for field in fields),
        enum_names=set(enum_names) if enum_names is not None else None,
        known_names=known_names,
        owner=rec.name,
    )

    lines: list[str] = [BANNER, "from __future__ import annotations\n\n"]
    if imports:
        lines.extend(imports)
        lines.append("\n")

    if any(field.uses_unresolved for field in fields):
        lines.append("Unresolved = Any\n\n")

    lines.append(f"class {rec.name}({base_model}):\n")
    if fields:
        for field in fields:
            lines.append(f"    {field.name}: {field.annotation} = {field.field_expr}\n")
    else:
        lines.append("    pass\n")

    lines.append(f"\n\n{rec.name}.model_rebuild(raise_errors=False)\n")
    return format_python("".join(lines))


def render_enum(rec: TypeRecord) -> str:
    """Render a standalone IntEnum module for one enum-shaped datatype record."""

    lines = [
        BANNER,
        "from __future__ import annotations\n\n",
        "from enum import IntEnum\n\n",
        _render_enum_class(rec),
    ]
    return format_python("".join(lines))


def render_enums(records: list[TypeRecord]) -> str:
    """Render all enum-shaped datatype records into one enums.py module."""

    lines = [BANNER, "from __future__ import annotations\n\n"]
    if records:
        lines.append("from enum import IntEnum\n\n")
        lines.append("\n\n".join(_render_enum_class(rec).rstrip() for rec in records))
        lines.append("\n")
    return format_python("".join(lines))


class _RenderedField:
    def __init__(
        self,
        *,
        name: str,
        annotation: str,
        field_expr: str,
        uses_unresolved: bool,
    ) -> None:
        self.name = name
        self.annotation = annotation
        self.field_expr = field_expr
        self.uses_unresolved = uses_unresolved


def _model_fields(
    rec: TypeRecord,
    known_names: set[str],
    *,
    force_optional: set[str] | None = None,
    all_optional: bool = False,
) -> list[_RenderedField]:
    rendered: list[_RenderedField] = []
    used_names: set[str] = set()
    for prop in rec.properties:
        raw_name = prop.get("name")
        if not isinstance(raw_name, str):
            continue
        name = field_name(raw_name)
        if name is None:
            continue

        name = _unique_field_name(name, used_names)
        annotation = python_type(prop, known_names)
        optional = (
            all_optional
            or _is_optional_property(prop)
            or (force_optional is not None and raw_name in force_optional)
        )
        alias = _string_literal(raw_name)
        if optional:
            annotation = f"{annotation} | None"
            field_expr = f"Field(default=None, alias={alias})"
        else:
            field_expr = f"Field(alias={alias})"
        rendered.append(
            _RenderedField(
                name=name,
                annotation=annotation,
                field_expr=field_expr,
                uses_unresolved="Unresolved" in _annotation_tokens(annotation),
            )
        )
    return rendered


def _unique_field_name(name: str, used_names: set[str]) -> str:
    if name not in used_names:
        used_names.add(name)
        return name

    suffix = 2
    while f"{name}_{suffix}" in used_names:
        suffix += 1
    unique = f"{name}_{suffix}"
    used_names.add(unique)
    return unique


def _is_optional_property(prop: Mapping[str, Any]) -> bool:
    if prop.get("required") is False or prop.get("nullable") is True:
        return True

    raw_type = prop.get("type")
    if not isinstance(raw_type, str) or not raw_type:
        return True

    normalized = " ".join(raw_type.lower().split())
    return "nullable true" in normalized or "required false" in normalized


def _string_literal(value: str) -> str:
    return json.dumps(value)


def _base_model(rec: TypeRecord, request_types: Collection[str] | None) -> str:
    if request_types is not None:
        return "RequestModel" if rec.name in request_types else "ResponseModel"
    return "RequestModel" if "RequestDTO" in rec.name else "ResponseModel"


def _model_imports(
    *,
    annotations: list[str],
    base_model: str,
    uses_field: bool,
    uses_unresolved: bool,
    enum_names: set[str] | None,
    known_names: set[str],
    owner: str,
) -> list[str]:
    lines: list[str] = []
    tokens = set().union(*(_annotation_tokens(annotation) for annotation in annotations))

    if "Decimal" in tokens:
        lines.append("from decimal import Decimal\n")

    known_deps = (tokens & known_names) - {owner}
    known_model_deps = sorted(known_deps - enum_names) if enum_names is not None else []
    guessed_enum_deps = sorted(known_deps) if enum_names is None else []
    typing_imports: list[str] = []
    if uses_unresolved or guessed_enum_deps:
        typing_imports.append("Any")
    if known_model_deps:
        typing_imports.append("TYPE_CHECKING")
    if typing_imports:
        lines.append(f"from typing import {', '.join(typing_imports)}\n")

    if uses_field:
        lines.append("from pydantic import Field\n")

    enum_deps = sorted((tokens & (enum_names or set())) - {owner})
    if enum_deps:
        lines.append(f"from .enums import {', '.join(enum_deps)}\n")

    for dependency in guessed_enum_deps:
        lines.append(
            f"try:\n"
            f"    from .enums import {dependency}\n"
            f"except ImportError:\n"
            f"    {dependency} = Any\n"
        )

    core_imports = [base_model]
    if "StoneXDateTime" in tokens:
        core_imports.append("StoneXDateTime")
    lines.append(f"from stonepy._core.models import {', '.join(core_imports)}\n")

    if known_model_deps:
        lines.append("\nif TYPE_CHECKING:\n")
        for dependency in known_model_deps:
            lines.append(f"    from .{dependency} import {dependency}\n")

    return lines


def _annotation_tokens(annotation: str) -> set[str]:
    return set(_IDENTIFIER_RE.findall(annotation))


def _render_enum_class(rec: TypeRecord) -> str:
    lines = [f"class {rec.name}(IntEnum):\n"]
    members = _enum_members(rec.properties)
    if not members:
        lines.append("    pass\n")
        return "".join(lines)

    used_names: set[str] = set()
    for label, code in members:
        member_name = _unique_field_name(python_name(label), used_names)
        lines.append(f"    {member_name} = {code}\n")
    return "".join(lines)


def _enum_members(properties: list[JsonObject]) -> list[tuple[str, int]]:
    members: list[tuple[str, int]] = []
    for prop in properties:
        raw_name = prop.get("name")
        raw_type = prop.get("type")
        if _is_integer_code(raw_type) and isinstance(raw_name, str) and raw_name.strip():
            type_code = cast(str, raw_type)
            members.append((raw_name, int(type_code.strip())))
        elif _is_integer_code(raw_name) and isinstance(raw_type, str) and raw_type.strip():
            name_code = cast(str, raw_name)
            members.append((raw_type, int(name_code.strip())))
    return members


def _is_integer_code(value: object) -> bool:
    return isinstance(value, str) and value.strip().isdigit()
